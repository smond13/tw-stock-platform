@st.cache_data(ttl=3600)
def get_tw_stock_list():
    tickers = []
    headers = {
        "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36",
        "Accept": "application/json, text/plain, */*",
    }

    # ── 上市：先用內建清單（去掉名稱為「台灣」的空殼，只留有意義的） ──
    seen_codes = set()
    for code, name in BUILTIN_TW:
        if code not in seen_codes and name != "台灣":
            tickers.append({"code":code,"name":name,"market":"上市","ticker":f"{code}.TW"})
            seen_codes.add(code)

    # ── 上市：嘗試從 API 補充更新名稱 ──
    try:
        r = requests.get(
            "https://openapi.twse.com.tw/v1/exchangeReport/STOCK_DAY_ALL",
            headers=headers, timeout=15
        )
        if r.status_code == 200:
            api_map = {}
            for item in r.json():
                c = str(item.get("Code","")).strip()
                n = str(item.get("Name","")).strip()
                if c.isdigit() and len(c) == 4 and n:
                    api_map[c] = n
            # 更新名稱 + 加入 API 有但內建沒有的
            for t in tickers:
                if t["market"] == "上市" and t["code"] in api_map:
                    t["name"] = api_map[t["code"]]
            for c, n in api_map.items():
                if c not in seen_codes:
                    tickers.append({"code":c,"name":n,"market":"上市","ticker":f"{c}.TW"})
                    seen_codes.add(c)
    except:
        pass

    # ── 上櫃：TPEx ──
    try:
        r2 = requests.get(
            "https://www.tpex.org.tw/openapi/v1/tpex_mainboard_daily_close_quotes",
            headers=headers, timeout=15
        )
        if r2.status_code == 200:
            for item in r2.json():
                code = str(item.get("SecuritiesCompanyCode","")).strip()
                name = str(item.get("CompanyName","")).strip()
                if code and len(code) == 4 and code not in seen_codes:
                    tickers.append({"code":code,"name":name,"market":"上櫃","ticker":f"{code}.TWO"})
                    seen_codes.add(code)
    except:
        pass

    # ── 上櫃備援 ──
    if len([t for t in tickers if t["market"]=="上櫃"]) < 100:
        try:
            r3 = requests.get(
                "https://www.tpex.org.tw/openapi/v1/tpex_mainboard_quotes",
                headers=headers, timeout=15
            )
            if r3.status_code == 200:
                for item in r3.json():
                    code = str(item.get("SecuritiesCompanyCode","")).strip()
                    name = str(item.get("CompanyName","")).strip()
                    if code and len(code) == 4 and code not in seen_codes:
                        tickers.append({"code":code,"name":name,"market":"上櫃","ticker":f"{code}.TWO"})
                        seen_codes.add(code)
        except:
            pass

    return tickers

@st.cache_data(ttl=300)
def get_stock_data(ticker, period="6mo"):
    try:
        hist = yf.Ticker(ticker).history(period=period)
        if hist is None or hist.empty:
            alt = ticker.replace(".TW",".TWO") if ".TW" in ticker else ticker.replace(".TWO",".TW")
            hist = yf.Ticker(alt).history(period=period)
        if hist is None or hist.empty: return None
        hist["MA5"]  = hist["Close"].rolling(5).mean()
        hist["MA10"] = hist["Close"].rolling(10).mean()
        hist["MA20"] = hist["Close"].rolling(20).mean()
        hist["Vol5"] = hist["Volume"].rolling(5).mean()
        return hist
    except: return None

