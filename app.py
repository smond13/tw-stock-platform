    pc="#ef4444" if pnl>=0 else "#22c55e"; ps="+" if pnl>=0 else ""
    c1,c2,c3,c4=st.columns(4)
    with c1: st.markdown(f'<div class="metric-card"><div class="metric-label">可用現金</div><div class="metric-value">${sim["cash"]:,.0f}</div></div>',unsafe_allow_html=True)
    with c2: st.markdown(f'<div class="metric-card"><div class="metric-label">持股市值</div><div class="metric-value">${total_mkt:,.0f}</div></div>',unsafe_allow_html=True)
    with c3: st.markdown(f'<div class="metric-card"><div class="metric-label">帳戶總值</div><div class="metric-value">${total_val:,.0f}</div></div>',unsafe_allow_html=True)
    with c4: st.markdown(f'<div class="metric-card"><div class="metric-label">總損益</div><div class="metric-value" style="color:{pc}">{ps}${pnl:,.0f}<br><span style="font-size:13px">{ps}{pnl_pct:.2f}%</span></div></div>',unsafe_allow_html=True)
    st.markdown("<br>",unsafe_allow_html=True)

    with st.sidebar:
        st.markdown('<div class="section-header">下單</div>',unsafe_allow_html=True)
        trade_raw=st.text_input("股票代號/名稱",placeholder="2330 / 台積電")
        trade_ticker,trade_name=resolve_ticker(trade_raw) if trade_raw else ("","")
        if trade_ticker and trade_ticker!=trade_raw.strip().upper(): st.caption(f"查詢：`{trade_ticker}`")
        auto_price=get_latest_price(trade_ticker) if trade_ticker else None
        trade_price=st.number_input("成交價（自動帶入收盤價）",min_value=0.01,value=float(auto_price) if auto_price else 100.0,step=0.1)
        trade_shares=st.number_input("股數",min_value=1,value=1000,step=1000)
        st.caption(f"預估金額：${trade_price*trade_shares:,.0f}")
        st.markdown("---")

        if st.button("🔴 買入",use_container_width=True):
            cost=trade_price*trade_shares
            if not trade_ticker: st.error("請輸入股票代號")
            elif cost>sim["cash"]: st.error(f"現金不足！需 ${cost:,.0f}")
            else:
                sim["cash"]-=cost
                ex=next((h for h in sim["holdings"] if h["ticker"]==trade_ticker),None)
                if ex:
                    ts=ex["shares"]+trade_shares
                    ex["cost"]=(ex["cost"]*ex["shares"]+trade_price*trade_shares)/ts
                    ex["shares"]=ts; ex["cur_price"]=trade_price
                else:
                    sim["holdings"].append({"ticker":trade_ticker,"name":trade_name,"shares":trade_shares,"cost":trade_price,"cur_price":trade_price})
